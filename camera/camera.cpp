void Gram_Schmidt_Process() {
	// 相机位置
	glm::vec3 cameraPos = glm::vec3(0.0f, 0.0f, 3.0f);

	glm::vec3 cameraTarget = glm::vec3(0.0f, 0.0f, 0.0f);
	// 从相机位置指向原点的单位向量
	glm::vec3 cameraVecDir = glm::normalize(cameraPos - cameraTarget);

	// 获取右向量
	// 先获取上向量 通过叉乘获取
	glm::vec3 up = glm::vec3(0.0f, 1.0f, 0.0f);
	glm::vec3 cameraRight = glm::normalize(glm::cross(up, cameraVecDir));

	//获取指向摄像机的正y
	glm::vec3 cameraUp = glm::cross(cameraVecDir, cameraRight);

	//创建lookat矩阵 glm::LookAt函数需要一个位置、目标和上向量 会创建一个类似于上面的观察矩阵
	glm::mat4 view = glm::mat4(1.0f);
	view = glm::lookAt(glm::vec3(0.0f, 0.0f, 3.0f), glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, 1.0f, 0.0f));
}

